    public static class 영구정지해제 extends CommandExecute {

        protected boolean hellban = false;

        private String getCommand() {
            if (hellban) {
                return "UnHellBan";
            } else {
                return "UnBan";
            }
        }

        @Override
        public int execute(MapleClient c, String[] splitted) {
            if (splitted.length < 2) {
                c.getPlayer().dropMessage(6, "[방법] !" + getCommand() + " <닉네임>");
                return 0;
            }
            byte ret;
            if (hellban) {
                ret = MapleClient.unHellban(splitted[1]);
            } else {
                ret = MapleClient.unban(splitted[1]);
            }
            if (ret == -2) {
                c.getPlayer().dropMessage(6, "[" + getCommand() + "] 쿼리 오류입니다.");
                return 0;
            } else if (ret == -1) {
                c.getPlayer().dropMessage(6, "[" + getCommand() + "] 해당 캐릭터가 존재하지 않습니다.");
                return 0;
            } else {
                c.getPlayer().dropMessage(6, "[" + getCommand() + "] 밴 해제가 성공하였습니다");

            }
            byte ret_ = MapleClient.unbanIPMacs(splitted[1]);
            if (ret_ == -2) {
                c.getPlayer().dropMessage(6, "[아이피밴] 쿼리 오류입니다");
            } else if (ret_ == -1) {
                c.getPlayer().dropMessage(6, "[아이피밴] 해당 캐릭터가 존재하지 않습니다.");
            } else if (ret_ == 0) {
                c.getPlayer().dropMessage(6, "[아이피밴] 해당 아이피나 맥이 존재하지 않습니다.");
            } else if (ret_ == 1) {
                c.getPlayer().dropMessage(6, "[아이피밴] 해당 아이피나 맥은 아이피밴 상태가 아닙니다.");
            } else if (ret_ == 2) {
                c.getPlayer().dropMessage(6, "[아이피밴] 아이피밴 해제가 완료되었습니다.");
            }
            return ret_ > 0 ? 1 : 0;
        }
    }

